# Program to calculate the area and perimeter of a rectangle
print("--- Rectangle Calculator ---")

# Taking user inputs
length = 5.0
width = 3.0

# Performing operations
area = length * width
perimeter = 2 * (length + width)

# Displaying outputs using standard print formatting
print("Length of Rectangle:", length)
print("Width of Rectangle:", width)
print("Calculated Area:", area)
print("Calculated Perimeter:", perimeter)
